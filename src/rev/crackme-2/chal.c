int flag[] = {0x44, 0x74, 0x6c, 0x7b, 0x74, 0x63, 0x68, 0x77, 0x5e, 0x51, 0x60, 0x65, 0x7c, 0x66, 0x4e, 0x66, 0x20, 0x60, 0x26, 0x6a, 0x64, 0x29, 0x46, 0x68, 0x73, 0x2d, 0x42, 0x13, 0x71, 0x41, 0x53, 0x5d, 0x53, 0x52, 0x14, 0x18, 0x49, 0x75, 0x1d, 0x40, 0x4c, 0x1e, 0x5f, 0x1, 0x5b, 0x5a, 0x5c, 0x49};
char buf[sizeof(flag) / sizeof(int)];

void encrypt_flag(){
    int len = sizeof(buf) / sizeof(char);
    for (int i = 0; i < len; i += 2){
        buf[i] = buf[i] ^ (i+3);
    }
    for (int i = 1; i < len; i += 2){
        buf[i] = buf[i] ^ (i+5);
    }
    for (int i = 0; i < len; i++){
        if (buf[i] != flag[i]){
            printf("Wrong flag!!!\n");
            return;
        }
    }
    printf("Correct flag!!!\n");
}

int main(){
    printf("Enter the flag: ");
    scanf("%s", buf);
    encrypt_flag();
    return 0;
}