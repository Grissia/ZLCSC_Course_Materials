def encrypt(flag, initial_steps):
    if len(initial_steps) != rotor_count:
        raise ValueError(f"Expected {rotor_count} seed numbers, got {len(initial_steps)}")

    res = ""
    current_rotors = [row[:] for row in wire_table[:rotor_count]]
    reflector = wire_table[rotor_count][:]

    for r in range(rotor_count):
        shift = initial_steps[r] % size
        current_rotors[r] = current_rotors[r][shift:] + current_rotors[r][:shift]

    for i in range(len(flag)):
        c = flag[i]
        if c not in charas:
            res += c
            continue
        
        for r in range(rotor_count):
            current_rotors[r] = current_rotors[r][1:] + current_rotors[r][:1]
            if (i + 1 + initial_steps[r]) % size != 0:
                break

        idx = charas.index(c)

        for r in range(9):
            idx = current_rotors[r][idx]

        idx = reflector[idx]

        for r in range(rotor_count - 1, -1, -1):
            idx = current_rotors[r].index(idx)

        res += charas[idx]

    return res

